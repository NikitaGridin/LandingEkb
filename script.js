const burger_btn = document.querySelector("#burger_btn");

burger_btn.addEventListener("click", () => {
  burger_menu.classList.add("block");
  burger_menu.classList.remove("hidden");
});

document.addEventListener("click", (event) => {
  if (
    (!event.target.closest("#burger_menu") &&
      !event.target.closest("#burger_btn")) ||
    event.target.closest("#burger_btn__close") ||
    event.target.closest("#burger_menu a")
  ) {
    burger_menu.classList.add("hidden");
  }
});

